# Arithmetic Operator
'''
variable_1 = 5
variable_2 = 3

print("variable_1 + variable_2 ->", variable_1 + variable_2)
print("variable_1 - variable_2 ->", variable_1 - variable_2)
print("variable_1 * variable_2 ->", variable_1 * variable_2)
print("variable_1 / variable_2 ->", variable_1 / variable_2)

print("variable_1 % variable_2 ->", variable_1 % variable_2)
print("variable_1 // variable_2 ->", variable_1 // variable_2)
print("variable_1 ** variable_2 ->", variable_1 ** variable_2)
'''
# Assignment Operator
'''
a = 6
a += 5
a -= 5
a *= 5
a /= 5
print(a)
'''
# Relational/Comparison/Conditional Operator
'''
a = 45
b = 23

print(a > b)
print(a < b)
print(a >= b)
print(a <= b)
print(a == b)
print(a != b)
'''

# Logical Operator
x = True
y = False

print("x and y -> ", x and y)
print("x or y -> ", x or y)
print("not x is -> ", not x)
print("not y is -> ", not y)
